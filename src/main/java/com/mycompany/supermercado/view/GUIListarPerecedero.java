    public void setProductosToTable(List <Perecedero> productos) {
        int i = 0;
        for (Perecedero producto : productos) {
            tableListado.setValueAt(producto.getCodigo(), i, 0);
            tableListado.setValueAt(producto.getNombre(), i, 1);
            tableListado.setValueAt(producto.getPrecio(), i, 2);
            tableListado.setValueAt(producto.getCantidad(), i, 3);
            tableListado.setValueAt(producto.getCaducidad(), i, 4);
            tableListado.setValueAt(producto.getPesoUnidad(), i, 5);
            i++;
        }
    }
